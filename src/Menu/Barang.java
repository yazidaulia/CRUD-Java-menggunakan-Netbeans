    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // Tombol Simpan
        String kb, nmb, jenis, harga, stk;
        kb = fKode.getText();
        nmb = fNama.getText();
        jenis = ComboJB.getSelectedItem().toString();
        harga = fHarga.getText();
        int hrg = Integer.parseInt(harga);
        stk = stok.getText();
        String sql = "insert into barang values('"+kb+"','"+nmb+"','"+jenis+"','"+hrg+"','"+stk+"')";
        try{
            Statement stat = conn.createStatement();
            stat.executeUpdate(sql);
            getData();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            resetAll();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Data tidak berhasil disimpan karena "+e);
        }
    }//GEN-LAST:event_bSimpanActionPerformed

