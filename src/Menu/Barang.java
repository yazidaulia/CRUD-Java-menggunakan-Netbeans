    public void getData(){
        String cariitem=txtCari.getText();
        try{
            dtm.getDataVector().removeAllElements();
            String s = "select * from barang where kd_barang like '"+cariitem+"%' or nama_barang like '"+cariitem+"%' or jenis_barang like '"+cariitem+"'";
            Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(s);
            while(rs.next()){
                Object[] obj = new Object[5];
                obj[0] = rs.getString("kd_barang");
                obj[1] = rs.getString("nama_barang");
                obj[2] = rs.getString("jenis_barang");
                obj[3] = rs.getString("stok");
                obj[4] = rs.getString("harga");
                
                dtm.addRow(obj);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }    
    }

