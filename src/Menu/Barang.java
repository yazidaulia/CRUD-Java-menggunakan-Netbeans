    private void bUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUbahActionPerformed
        // Tombol Ubah
        String sql = "update barang set nama_barang=?, jenis_barang=?, harga=?, stok=? where kd_barang='"+fKode.getText()+"'";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, fNama.getText());
            stat.setString(2, ComboJB.getSelectedItem().toString());
            stat.setString(3, fHarga.getText());
            stat.setString(4, stok.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil diubah");
            getData();
            resetAll();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "data tidak berhasil diubah "+e);
        }
    }//GEN-LAST:event_bUbahActionPerformed

    private void tblBarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBarangMouseClicked
        // Ketika tabel Barang di klik
        int baris = tblBarang.getSelectedRow();
        String a = dtm.getValueAt(baris, 0).toString();
        String b = dtm.getValueAt(baris, 1).toString();
        String c = dtm.getValueAt(baris, 2).toString();
        String d = dtm.getValueAt(baris, 3).toString();
        String e = dtm.getValueAt(baris, 4).toString();

        fKode.setText(a);
        fNama.setText(b);
        ComboJB.setSelectedItem(c);
        fHarga.setText(e);
        stok.setText(d);
    }//GEN-LAST:event_tblBarangMouseClicked

    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        // Tombol Hapus
        int row = tblBarang.getSelectedRow();
        String selected = dtm.getValueAt(row,0).toString();
        try{
            PreparedStatement ps = conn.prepareStatement("delete from barang where kd_barang='"+selected+"'");
            ps.executeUpdate();
            dtm.removeRow(row);
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Data tidak berhasil dihapus karena "+e);
        }
    }//GEN-LAST:event_bHapusActionPerformed

