    public void datatable(){
        Object[] Baris = {"ID Penjual","Nama Penjual", "No. Telepon", "Alamat"};
        model = new DefaultTableModel(null,Baris);
        String cariitem=txtcari.getText();
        try{
            String sql = "Select * from penjual where id_penjual like '%"+cariitem+"%' or nama_penjual like '%"+cariitem+"%' order by id_penjual asc";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                model.addRow(new Object[]{
                   hasil.getString(1),
                   hasil.getString(2),
                   hasil.getString(3),
                   hasil.getString(4),
                });
            }
            tblPenjual.setModel(model);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "data gagal dipanggil "+e);
        }
    }
