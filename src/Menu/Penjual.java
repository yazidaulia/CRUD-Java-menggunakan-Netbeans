    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        int row = tblPenjual.getSelectedRow();
        String selected = model.getValueAt(row,0).toString();
        try{
            PreparedStatement ps = conn.prepareStatement("delete from penjual where id_penjual='"+selected+"'");
            ps.executeUpdate();
            model.removeRow(row);
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
            resetAll();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Data tidak berhasil dihapus karena "+e);
        }
    }//GEN-LAST:event_bHapusActionPerformed

